        <h4>Indikator Pertumbuhan Lahir sampai Usia 24 Bulan</h4>

        <!-- Developmental milestones as bullet points -->
        <ul style="text-align: left; list-style-type: disc; padding-left: 20px;">
            <li><strong>0 - 1 Bulan:</strong> Bereaksi terhadap suara bel.</li>
            <li><strong>1 - 2 Bulan:</strong> Membalas senyuman, tersenyum spontan, mengoceh, mengangkat kepala 45 derajat ketika tengkurap, mata mengikuti benda yang digerakkan.</li>
            <li><strong>2 - 3 Bulan:</strong> Tertawa, berteriak, tangan saling berpegangan, mengangkat kepala 90 derajat ketika tengkurap.</li>
            <li><strong>3 - 4 Bulan:</strong> Kepala tegak ketika didudukan, memandang tangannya sendiri, memegang mainan, menoleh ke kanan dan ke kiri.</li>
            <li><strong>4 - 5 Bulan:</strong> Mengangkat dada ketika tengkurap, tengkurap dan telentang sendiri, mengamati benda-benda kecil, menoleh ke suara kerincingan.</li>
            <li><strong>5 - 6 Bulan:</strong> Berusaha meraih mainan, menoleh ketika dipanggil Namanya, memasukan tangan/biskuit ke mulut.</li>
            <li><strong>6 - 7 Bulan:</strong> Duduk tanpa dipegang, mencari benda jatuh/disembunyikan, mengucapkan satu kata: ba, pa, ma.</li>
            <li><strong>7 - 8 Bulan:</strong> Memegang dua mainan dengan tangan kanan-kiri, mengucapkan suku kata bersambung: bababababa, berdiri dipegangi.</li>
            <li><strong>9 - 12 Bulan:</strong> Berdiri sendiri tanpa dibantu, melambaikan tangan (bye-bye), menunjuk untuk meminta sesuatu, memanggil mama-papa.</li>
            <li><strong>12 - 15 Bulan:</strong> Menirukan kegiatan: menyapu/mengepel, memasukan kubus ke gelas, berbicara 1 kata, berjalan, mencoret-coret.</li>
            <li><strong>15 - 18 Bulan:</strong> Minum dari cangkir, berbicara tiga kata, menumpuk dua kubus, berlari.</li>
            <li><strong>18 - 24 Bulan:</strong> Berbicara enam kata, menunjukkan dua benda/gambar sesuai Namanya, menggabungkan beberapa kata, menunjukkan bagian tubuh yang ditanyakan, menyebutkan nama benda/gambar, menumpuk empat sampai dengan lima kubus.</li>
        </ul>

